    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int res=this.validateInput();
        if(res==0)
        {
            JOptionPane.showMessageDialog(null, "Please enter all fields to continue","Invalid Input",JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(res==-1)
        {
            JOptionPane.showMessageDialog(null, "Password should be atleast of 4 characters","Invalid Input",JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(res==-2)
        {
            JOptionPane.showMessageDialog(null, "Passwords do not match","Invalid Input",JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            boolean resSql=UserDao.changePassword(UserProfile.getUsername(),String.valueOf(txtPwd.getPassword()));
            if(resSql)
                JOptionPane.showMessageDialog(null, "Password Changed Successfully","SUCCESS!!!!",JOptionPane.DEFAULT_OPTION);
            else
                JOptionPane.showMessageDialog(null, "Password not changed","FAILED!!!!",JOptionPane.ERROR_MESSAGE);
        }
        catch(SQLException ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error Retriving data from database","Database Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

