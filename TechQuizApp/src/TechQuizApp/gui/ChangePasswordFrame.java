    public int validateInput(){
        char[] pwd=txtPwd.getPassword();
        char[] repwd=txtRePwd.getPassword();
        if(pwd.length==0||repwd.length==0){
            return 0;
        }
        if(pwd.length<4||repwd.length<4){
            return -1;
        }
        String pwdS=String.valueOf(pwd);
        String repwdS=String.valueOf(repwd);
        if(!(pwdS.equals(repwdS)))
            return -2;
        return 1;
    }

