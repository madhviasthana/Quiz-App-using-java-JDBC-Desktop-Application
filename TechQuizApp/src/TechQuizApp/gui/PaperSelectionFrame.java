    private void btnTTTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTTTActionPerformed
        if(!validateInput()){
            JOptionPane.showMessageDialog(null, "Select proper Options to continue", "Invalid Input", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(jcbExamId.getItemCount()==0){
            JOptionPane.showMessageDialog(null, "Please select an exam ID to continue", "Invalid Input", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int res=JOptionPane.showConfirmDialog(null, "You have selected "+jcbSubject.getSelectedItem().toString()+", "+jcbExamId.getSelectedItem().toString(),"Confirmation",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        if(res==JOptionPane.YES_OPTION){
            try{
            int qno=ExamDao.getQuestionCountByExam(jcbExamId.getSelectedItem().toString());
            ExamPojo exm=new ExamPojo(jcbExamId.getSelectedItem().toString(),jcbSubject.getSelectedItem().toString(),qno);
            TakeTestFrame ttf=new TakeTestFrame(exm);
            ttf.setVisible(true);
            this.dispose();
            }
            catch(SQLException ex){
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "Error retriving data from database ", "Database Error!", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnTTTActionPerformed

