    private void jcbSubjectItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcbSubjectItemStateChanged
        if(evt.getStateChange()==ItemEvent.DESELECTED)
            return;
        boolean res=this.validateInput();
        
        if(!res){
            JOptionPane.showMessageDialog(null, "Please select a subject to continue.","Error!",JOptionPane.ERROR_MESSAGE);
            return;
        }
        jcbExamId.removeAllItems();
        try{
            if(!ExamDao.isPaperSet(subject)){
                JOptionPane.showMessageDialog(null, "No Exam set for "+subject, "Subject not found!", JOptionPane.ERROR_MESSAGE);
                return;
            }
            ArrayList<String> examList=ExamDao.getExamIdBySubject(UserProfile.getUsername(),subject);
            jcbExamId.removeAllItems();
            if(examList.isEmpty()){
                JOptionPane.showMessageDialog(null, "You have given all exams of "+subject+", try another subject.", "Error!", JOptionPane.ERROR_MESSAGE);
                return;
            }
            for(String s:examList){
                jcbExamId.addItem(s);
        }
        }
        catch(SQLException e)
                    {
                    e.printStackTrace();
                    JOptionPane.showMessageDialog(null, "Error Retriving data from DataBase", "Database Error", JOptionPane.ERROR_MESSAGE);
                    }
    }//GEN-LAST:event_jcbSubjectItemStateChanged

